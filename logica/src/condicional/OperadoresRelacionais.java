package condicional;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		int x=5;
		
		System.out.println(x==5); //Verifica se x é igual a 5.
		System.out.println(x!=5); //Verifica se x é diferente de 5.
		System.out.println(x>10); //Verifica se x é maior que 10.
		System.out.println(x<10); //Verifica se x é menor que 10.
		System.out.println(x>=10); //Verifica se x é maior ou igual a 10.
		System.out.println(x<=10); //Verifica se x é menor ou igual a 10.
	}

}
